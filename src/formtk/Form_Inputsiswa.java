    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if(txtIdpelanggan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "ID Mobil Kosong");
        }else if(txtnamapelnggan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Merk Kosong");
        }else if(txtnmrtel.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Usia Mobil Kosong");
        }else if(txtalamat.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "No Rangka Kosong");
        }else{
            String vIdPelanggan = String.valueOf(txtIdpelanggan.getText());
            String vNama = String.valueOf(txtnamapelnggan.getText());
            String vNoTel = String.valueOf(txtnmrtel.getText());
            String vAlamat = String.valueOf(txtalamat.getText());
            try {
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("insert into datamobil (idmobil,merk,usiamobil,noRangka)"
                        + "values ('"+vIdPelanggan+"','"+vNama+"','"+vNoTel+"','"+vAlamat+"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Tersimpan");
                txtIdpelanggan.setText("");
                txtnamapelnggan.setText("");
                txtnmrtel.setText("");
                txtalamat.setText("");
                txtIdpelanggan.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Tersimpan");
            }
        }
        LebarKolom();
        ViewData();
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprintActionPerformed
        try {
            String file = "src/formtk/datasis.jasper";
            HashMap parameter = new HashMap();
            File reportFile = new File(file);
            JasperReport jsr = (JasperReport) JRLoader.loadObject(null, reportFile);
            JasperPrint jsp = JasperFillManager.fillReport(jsr, parameter, KoneksiDB.getConnection());
            JasperViewer.viewReport(jsp,false);
            JasperViewer.setDefaultLookAndFeelDecorated(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnprintActionPerformed

