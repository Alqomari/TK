    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int pesan = JOptionPane.showConfirmDialog(null, "Hapus Data", "Konfirmasi",JOptionPane.YES_NO_OPTION);
        if(pesan==0){
            int row = tbdatapelanggan.getSelectedRow();
            String id = tbdatapelanggan.getValueAt(row, 0).toString();
            try {
                //Statement statement = (Statement)KoneksiDB.getConnection().createStatement();
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("delete from datamobil where idmobil = '"+id+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                ViewData();
                LebarKolom();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Berhasil Dihapus");
            }
        }
        ViewData();
    }//GEN-LAST:event_btnhapusActionPerformed

