    private void ViewData(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Id ");
        tbl.addColumn("Nama");
        tbl.addColumn("Usia");
        tbl.addColumn("Alamat");
        tbdatapelanggan.setModel(tbl);
        LebarKolom();
        try {
           com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
           ResultSet rs = statement.executeQuery("select * from datamobil");
           while(rs.next()){
               tbl.addRow(new Object[]{
                   rs.getString("idmobil"),
                   rs.getString("merk"),
                   rs.getString("usiamobil"),
                   rs.getString("noRangka"),
               });
               tbdatapelanggan.setModel(tbl);
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

