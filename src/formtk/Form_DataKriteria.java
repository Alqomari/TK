    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if(txtIdkriteria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "ID Kriteria Kosong");
        }else if(txtnamakriteria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Kriteria Kosong");
        }else if(txtbobot.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Bobot Kosong");
        }else{
            String vIdKriteria = String.valueOf(txtIdkriteria.getText());
            String vNamaKriteria = String.valueOf(txtnamakriteria.getText());
            String vBobot = String.valueOf(txtbobot.getText());
            try {
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("insert into kriteria (IDKriteria,NamaKriteria,bobot)"
                        + "values ('"+vIdKriteria+"','"+vNamaKriteria+"','"+vBobot+"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Tersimpan");
                txtIdkriteria.setText("");
                txtnamakriteria.setText("");
                txtbobot.setText("");
                txtIdkriteria.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Tersimpan");
            }
        }
        LebarKolom();
        ViewData();
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        if(txtIdkriteria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "ID Pelanggan Kosong");
        }else if(txtnamakriteria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Kosong");
        }else if(txtbobot.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Bobot Kosong");
        }else{
            String vIdKriteria = String.valueOf(txtIdkriteria.getText());
            String vNamaKriteria = String.valueOf(txtnamakriteria.getText());
            String vBobot = String.valueOf(txtbobot.getText());
            try {
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("update kriteria set NamaKriteria = '"+vNamaKriteria+"',bobot = '"+vBobot+"'"
                        + "where IDKriteria = '"+vIdKriteria+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Tersimpan");
                txtIdkriteria.setText("");
                txtnamakriteria.setText("");
                txtbobot.setText("");
                txtIdkriteria.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Tersimpan");
            }
        }
        ViewData();
    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int pesan = JOptionPane.showConfirmDialog(null, "Hapus Data", "Konfirmasi",JOptionPane.YES_NO_OPTION);
        if(pesan==0){
            int row = tbdatakriteria.getSelectedRow();
            String id = tbdatakriteria.getValueAt(row, 0).toString();
            try {
                //Statement statement = (Statement)KoneksiDB.getConnection().createStatement();
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("delete from kriteria where IDKriteria = '"+id+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                ViewData();
                LebarKolom();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Berhasil Dihapus");
            }
        }
        ViewData();
    }//GEN-LAST:event_btnhapusActionPerformed

