    private void ViewData(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Id Kriteria");
        tbl.addColumn("Nama Kriteria");
        tbl.addColumn("Bobot");
        tbdatakriteria.setModel(tbl);
        LebarKolom();
        try {
           com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
           ResultSet rs = statement.executeQuery("select * from kriteria");
           while(rs.next()){
               tbl.addRow(new Object[]{
                   rs.getString("IDKriteria"),
                   rs.getString("NamaKriteria"),
                   rs.getString("Bobot"),
               });
               tbdatakriteria.setModel(tbl);
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

