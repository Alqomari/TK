    private void txtidpelngganKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidpelngganKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                txtusia.requestFocus();
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                ResultSet rs = statement.executeQuery("select * from datamobil where IDmobil like '%"+txtnmplg.getText()+"%'");
                while(rs.next()){
                    txtnmplg.setText(rs.getString("merk"));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Form_InputNilaiKriteria.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtidpelngganKeyPressed

