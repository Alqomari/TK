    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

           
        if(txtIdnilai.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "ID Nilai Kosong");
        }else if(txtidpelnggan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "ID Mobil Kosong");
        }else if(txtnmplg.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Merk Kosong");
        }else if(txtusia.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Jarak Kosong");
        }else if(txtpendapatan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Harga Kosong");
        }else if(jCRKredit.getSelectedItem().equals("pilih")){
            JOptionPane.showMessageDialog(null, "Anda belum memilih usia mobil");
        }else if(txttanggungan.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Konsumsi BBM Kosong");
        }else{
            String vIdNilai = String.valueOf(txtIdnilai.getText());
            String vIdPelanggan = String.valueOf(txtidpelnggan.getText());
            String vNama = String.valueOf(txtnmplg.getText());
            String vUsia = String.valueOf(txtusia.getText());
            String vPendapatan = String.valueOf(txtpendapatan.getText());
            String vTanggungan = String.valueOf(txttanggungan.getText());
            try {
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement)KoneksiDB.getConnection().createStatement();
                statement.executeUpdate("insert into nilaikriteria (IDNilaiKriteria,IDmobil,merk, jarak,harga,usiamobil,bbm)"
                        + "values ('"+vIdNilai+"','"+vIdPelanggan+"','"+vNama+"','"+vUsia+"','"+vPendapatan+"','"+jCRKredit.getSelectedItem().toString()+"','"+vTanggungan+"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Tersimpan");
                txtIdnilai.setText("");
                txtidpelnggan.setText("");
                txtnmplg.setText("");
                txtusia.setText("");
                txtpendapatan.setText("");
                txttanggungan.setText("");
                txtIdnilai.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Tidak Tersimpan");
            }
        }
        LebarKolom();
        ViewData();
    }//GEN-LAST:event_btnsimpanActionPerformed

